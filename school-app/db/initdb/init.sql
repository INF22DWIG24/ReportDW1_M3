create schema IF NOT EXISTS schoolapp;
use schoolapp;

CREATE TABLE `professor` (
   `id` int(11) NOT NULL AUTO_INCREMENT,
   `nome` varchar(512) NOT NULL,
   PRIMARY KEY (`id`)
 ) ENGINE=InnoDB
 ;

CREATE TABLE `curso` (
   `id` int(11) NOT NULL AUTO_INCREMENT,
   `nome` varchar(512) NOT NULL,
   `nrAnos` int(11) DEFAULT NULL,
   PRIMARY KEY (`id`)
 ) ENGINE=InnoDB
 ;
 
 CREATE TABLE `disciplina` (
   `id` int(11) NOT NULL AUTO_INCREMENT,
   `ano` int(11) DEFAULT NULL,
   `nome` varchar(512) NOT NULL,
   `cursoId` int(11) NOT NULL,
   `professorId` int(11) NOT NULL,
   PRIMARY KEY (`id`),
   CONSTRAINT fk_profdisc
	FOREIGN KEY (`professorId`)
    REFERENCES professor(`id`),
CONSTRAINT fk_cursodisc
	FOREIGN KEY (`cursoId`)
    REFERENCES curso(`id`)
 ) ENGINE=InnoDB
 ;

CREATE TABLE `aluno` (
   `id` int(11) NOT NULL AUTO_INCREMENT,
   `nome` varchar(512) NOT NULL,
   `nAluno` varchar(512) NOT NULL,
   `disciplinaId` int(11) DEFAULT NULL,
   PRIMARY KEY (`id`),
   CONSTRAINT fk_alunodisc
	FOREIGN KEY (`disciplinaId`)
    REFERENCES disciplina(`id`)
 ) ENGINE=InnoDB
 ;
 
 
